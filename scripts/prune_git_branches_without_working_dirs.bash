#!/usr/bin/env bash
############################################################################################################################################################
#
############################################################################################################################################################

############################################################################################################################################################
# Read input param
############################################################################################################################################################
PARAM="${*}"
echo "Param: ${PARAM}"

############################################################################################################################################################
# Define branch time to clean
# TODO: get this value from command line
############################################################################################################################################################
# BRANCH_TYPE="worktree"
# BRANCH_TYPE="bugfix"
# BRANCH_TYPE="feature"
# BRANCH_TYPE="docs"
# BRANCH_TYPE="cursor"
BRANCH_TYPE="tooling"
echo "Branch Type: \"${BRANCH_TYPE}\""


############################################################################################################################################################
# Update current directory
############################################################################################################################################################
CURRENT_PWD="$(pwd)"
echo "Current PWD: ${CURRENT_PWD}"


############################################################################################################################################################
# Debug output
############################################################################################################################################################
echo "git branch:"
git branch
echo "git branch, exclude open branches:"
git branch | grep -v -e "^(\+\*)\ .*$"
echo "git branch, exclude open branches, include only worktree branches:"
# git branch | grep -v -e "^+\ ${BRANCH_TYPE}" | grep "worktree*"
git branch | grep -v -e "^(\+\*)\ .*$" | grep "${BRANCH_TYPE}"


############################################################################################################################################################
# Close worktrees and branches
############################################################################################################################################################
# for i in $(git branch | grep -v -e "^+\ ${BRANCH_TYPE}" | grep "worktree*"); do
# for i in $(git branch | grep -v -e "^+\ ${BRANCH_TYPE}" | grep "worktree*"); do
# for i in $(git branch | grep -v -e "^+\ .*$" | grep "worktree*"); do
for i in $(git branch | grep -v -e "^(\*\+)\ .*$" | grep "${BRANCH_TYPE}"); do
    echo "Raw Branch (i): ${i}"
    TREE_DIR="${CURRENT_PWD}/.claude/${i/-/s\/}"
    echo "Tree Dir: ${TREE_DIR}"
    if [[ -d "${TREE_DIR}" ]]; then
        echo "Valid Dir: ${TREE_DIR}"
        cd "${TREE_DIR}" || continue
        git status
    else
        echo "Not Found: ${TREE_DIR} -- closing branch"
        if [[ ( "${PARAM}" != "" ) && ( ( "${PARAM}" == "-D" ) || ( "${PARAM}" == "-F" ) ) ]]; then
            echo "Deleting Branch with --Force"
            git branch -D "${i}"
        else
            echo "Performing Standard Branch Delete"
            git branch -d "${i}"
        fi
    fi
done
